"""

Trocando o valor entre variáveis em Python

"""
# Ex1
x = 10  # Rafael
y = 'Rafael'  # 10
x, y = y, x  # Forma de trocar valores entre variáveis no Python

# Forma padrão de outras linguagens para trocar valores entre variáveis.
# z = x
# x = y
# y = z

print(f'x = {x} e y = {y}')

# Ex2
x = 10  # Rafael
y = 'Rafael'  # 10
z = 'Angelo'
x, y, z = z, x, y

print(f'x = {x} e y = {y} e z = {z}')
